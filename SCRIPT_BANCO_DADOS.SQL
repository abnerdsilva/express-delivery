	DATA_PEDIDO DATETIME NOT NULL DEFAULT GETDATE(),
	DATA_ENTREGA DATETIME,
	VR_TOTAL DECIMAL(10,2) DEFAULT 0.00,
	VR_TAXA DECIMAL(10,2) DEFAULT 0.00,
	VR_TROCO DECIMAL(10,2) DEFAULT 0.00,
