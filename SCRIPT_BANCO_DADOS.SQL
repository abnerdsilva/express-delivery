	CPF VARCHAR(15),
	RG VARCHAR(15),
	LOGRADOURO VARCHAR(150) NOT NULL,
	NUMERO INT NOT NULL,
	BAIRRO VARCHAR(150) NOT NULL,
	CIDADE VARCHAR(50) NOT NULL,
	ESTADO VARCHAR(2) NOT NULL,
	CEP INT NOT NULL,
	STATUS_CLIENTE BIT NOT NULL DEFAULT 1,
